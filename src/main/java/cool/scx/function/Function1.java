package cool.scx.function;

/// 设计哲学与背景说明:
/// 
/// 1. 函数式接口的存在是 Java 语言的一个 "不得已而为之" 的产物.
///    Java 本身对函数作为一等公民的支持远不如一些函数式语言,
///    因此需要通过函数式接口来模拟函数行为, 尤其是在 Lambda 引入之前.
///
/// 2. 命名的哲学:
///    一个函数式接口的命名, 要么做到绝对细化到具体业务层, 明确语义;
///    要么彻底保持中立, 通用, 避免像 JDK 自带的接口那样处于语义的尴尬地带.
///    如 JDK 自带的 Supplier, Consumer 等接口, 命名模糊，既不中立也不具体,
///    使用时语义不够直观, 增加认知负担.
///
/// 3. 异常传播的重要性:
///    Java 标准的函数式接口不支持抛出受检异常, 这极大限制了它们在实际业务中对异常的处理能力.
///    支持泛型异常抛出, 可以让函数式接口更好地适配复杂的业务逻辑和异常处理流程,
///    避免不必要的异常封装和侵入式的异常捕获.
///
/// 4. 关于基本类型特化:
///    虽然基本类型特化可以避免装箱拆箱带来的性能损失,
///    但全面特化所有参数和返回值会导致类的数量呈指数级爆炸, 达到不可维护的规模 (理论上亿级别).
///    经过权衡, 我们放弃全面特化.
///    当然随着 Java 的升级可能有一天会直接支持基本类型泛型.
///
/// 5. 命名规则说明:
///    - 接口名称采用绝对中立的统一格式: Function + 参数个数 + (Void 表示无返回值)
///      例如 Function2 表示两个参数有返回值的函数，Function3Void 表示三个参数无返回值的函数.
///    - 泛型参数统一按字母顺序排列 (A, B, C, D...), 异常类型统一为 X extends Throwable.
///    - 方法名固定为 apply, 保持接口简洁一致.
///
/// 6. 相比 Java 自带接口的优势:
///    - 语义更加明确, 仅表示绝对中立的纯函数, 避免含糊不清的命名.
///    - 支持受检异常传播, 提升异常处理能力.
///    - 命名结构统一, 便于快速识别和使用.
///    - 避免因过度特化导致的类膨胀问题, 保持代码库轻量.
@FunctionalInterface
public interface Function1<A, R, X extends Throwable> {

    R apply(A a) throws X;

}
